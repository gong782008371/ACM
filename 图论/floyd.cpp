//**********************************
//任意两点间的最短路，不可以有负权边
//两个吊之间没有边时，dis为INF
//**********************************
void flyod()
{
    for(int k=0;k<N;k++)
    {
        for(int i=0;i<N;i++)
        {
            for(int j=0;j<N;j++)
            {
                if(d[i][j] > d[i][k] + d[k][j])
                {
                    d[i][j] = d[i][k] + d[k][j];
                }
            }
        }
    }
}
